import pinkFloyd.TheWall;
import students.Student;
import students.StudentCheater;
import students.StudentRandom;

import java.rmi.AccessException;
import java.util.ArrayList;
import java.util.Collections;
import java.util.Comparator;

import static util.Util10.giveRandomString;

public class Lecture10{
	public static void main(String[] args) throws AccessException{
		//setSeed(4);

		System.out.println("==== Invite Pink Floyd ====");
		final TheWall theWall = new TheWall();//the maze with The Wall ;-)

		System.out.println("==== Add students on The Wall ====");
		System.out.println("'0','1',.. - each student's position behind The Wall");
		ArrayList<Student> students = new ArrayList<>(4);
		final int id1 = theWall.regStudent();
		students.add(new StudentCheater(id1,"\"Cheater " + id1
				+ ' ' + giveRandomString(5) + '\"', theWall));
		for(int i=4; 0<=--i;){
			final int id = theWall.regStudent();
			students.add(new StudentRandom(id, "\"Random " + id
					+ ' ' + giveRandomString(6) + '\"', theWall));
		}

		System.out.println("==== Force students run on The Wall ====");
		students.forEach(student -> {
			System.out.println(student.getName() + " is ready! start");
			student.thread = new Thread(student, student.getName());
			student.thread.start();
		});
		students.forEach(student -> {
			try{
				student.thread.join();
			}catch(InterruptedException e){
				e.printStackTrace();
			}
		});
		System.out.println("==== The students have finished ====");
		students.forEach(student -> System.out.println(student.getName()
				+ " made " + student.getSteps() + " steps"
				+ " for time(ms) " + student.getTime()));
		System.out.println("==== Only one student is a winner ====");
		final Student winner = Collections.min(students
				, Comparator.comparing(Student::getSteps));
		System.out.println("The winner is ....., yes! " + winner.getName() + "! Congrats!");
	}
}
